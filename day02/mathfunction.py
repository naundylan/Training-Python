########## math library
import math
# or
from math import *

# print(help(math))

# -> sqrt: returned value is a float
print(sqrt(16))  # Output: 4.0

# -> isqrt: returned value is a int
print(isqrt(36)) # output: 6

# -> pow: returned value is a float
print(pow(2,10))
print(pow(2, 1/3))

# -> round floor ceil
print(round(3.7)) # output: 4
print(ceil(3.4)) #output: 4
print(floor(3.4)) # output: 3

# factorial giai thừa
print(factorial(5))

# gcd(a,b) ước chung LN
print(gcd(100, 450))

############# built-in-func ##############
#abs fabs min max sum
print(sum([1,2,3,4,5])) # 15
